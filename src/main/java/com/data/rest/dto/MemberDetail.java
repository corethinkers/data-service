
package com.data.rest.dto;

public class MemberDetail {

    public String amount;
    public String assignedId;
    public String coverageEffectiveDate;
    public String coverageOptionCode;
    public String premium;
    public String firstName;
    public String lastName;
    public String memberId;
    public String supplierId;
    
	/**
	 * @return the amount
	 */
	public String getAmount() {
		return amount;
	}
	/**
	 * @param amount the amount to set
	 */
	public void setAmount(String amount) {
		this.amount = amount;
	}
	/**
	 * @return the assignedId
	 */
	public String getAssignedId() {
		return assignedId;
	}
	/**
	 * @param assignedId the assignedId to set
	 */
	public void setAssignedId(String assignedId) {
		this.assignedId = assignedId;
	}
	/**
	 * @return the coverageEffectiveDate
	 */
	public String getCoverageEffectiveDate() {
		return coverageEffectiveDate;
	}
	/**
	 * @param coverageEffectiveDate the coverageEffectiveDate to set
	 */
	public void setCoverageEffectiveDate(String coverageEffectiveDate) {
		this.coverageEffectiveDate = coverageEffectiveDate;
	}
	/**
	 * @return the coverageOptionCode
	 */
	public String getCoverageOptionCode() {
		return coverageOptionCode;
	}
	/**
	 * @param coverageOptionCode the coverageOptionCode to set
	 */
	public void setCoverageOptionCode(String coverageOptionCode) {
		this.coverageOptionCode = coverageOptionCode;
	}
	/**
	 * @return the premium
	 */
	public String getPremium() {
		return premium;
	}
	/**
	 * @param premium the premium to set
	 */
	public void setPremium(String premium) {
		this.premium = premium;
	}
	/**
	 * @return the firstName
	 */
	public String getFirstName() {
		return firstName;
	}
	/**
	 * @param firstName the firstName to set
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	/**
	 * @return the lastName
	 */
	public String getLastName() {
		return lastName;
	}
	/**
	 * @param lastName the lastName to set
	 */
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	/**
	 * @return the memberId
	 */
	public String getMemberId() {
		return memberId;
	}
	/**
	 * @param memberId the memberId to set
	 */
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	/**
	 * @return the supplierId
	 */
	public String getSupplierId() {
		return supplierId;
	}
	/**
	 * @param supplierId the supplierId to set
	 */
	public void setSupplierId(String supplierId) {
		this.supplierId = supplierId;
	}
    
}
